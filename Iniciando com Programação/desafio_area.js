// Área da circunferência
const PI = 3.141592;
const raio = 10;
let area  = PI * raio * raio;

console.log('A área da circunferência é ' + area 
+ ' m2.');

area = Math.PI * raio * raio;
console.log('A área da circunferência é ' + area 
+ ' m2.');

area = Math.PI * Math.pow(raio, 2)
console.log('A área da circunferência é ' + area 
+ ' m2.');